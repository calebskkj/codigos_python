a = int(input("informe o primeiro valor numérico: "))
b = int(input("informe o segundo valor numérico: "))
c = int(input("informe o terceiro valor numérico: "))

if a+b<c:
    print(f"o valor da soma do primeiro valor {a} e o segundo valor {b} é menor que {c}.")

else: 
    print(f"o valor da soma do primeiro valor {a} e o segundo valor {b} é maior ou igual que {c}.")